a = 4
b = 2

#lebih besar dari >
print('=========lebih besar dari (<)')
hasil = a > 3
print(a,'>',3,'=',hasil)
hasil =  b> 3
print(b,'>',3,'=',hasil)
hasil =  b> 2
print(b,'>',2,'=',hasil)

print('========= kurang dari (<)')
hasil = a < 3
print(a,'<',3,'=',hasil)
hasil =  b< 3
print(b,'<',3,'=',hasil)
hasil =  b< 2
print(b,'<',2,'=',hasil)